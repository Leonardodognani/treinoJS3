// Operadores lógicos (logical operators)

// -2 valores Booleanos, quando verificados, resultará em verdadeiro ou falso

let pao = true
let queijo = true

// AND &&
console.log(pao && queijo) // imprime true, pq ambos são verdadeiros

// OR ||
console.log(pao || queijo) // imprime true, pois ou o primeiro ou o segundo são verdadeiros

// NOT !
console.log(!pao) // imprime falso, porque ele nega o valor.