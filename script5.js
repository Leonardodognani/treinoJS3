// Grouping Operator ( )

let total = 2 + 3 * 5 // ele vai seguir a ordem aritmética de preferencia, como nos calculos normais
console.log(total) // imprime 17

let total2 = (2 + 3) * 5 // aqui nós alteramos a ordem na qual os cálculos são efetuados, com parênteses. 
console.log(total2) // imprime 25

